<?php
	/*
		Кузнецов М.В., Симдянов И.В. - PHP. Практика создания Web-сайтов (2-е издание) - 2009
		Листинг 3.36. Загрузка страницы с использованием расширения CURL - стр. 254
	 */

	// Задаём адрес удалённого сервера
	$curl = curl_init("http://www.php.net");
	// Устанавливаем параметры соединения
	curl_setopt($curl, CURLOPT_RETURNTRANSFER, 1);
	// Получаем содержимое страницы
	$content = curl_exec($curl);
	// Закрываем CURL-соединение
	curl_close($curl);
	
	echo $content;
?>